// Objetivo: exibir caracteres de uma classe [ string ] de forma filtrada, escondendo chars

const palavra = '_Cod3r_'

// Como eu devo pensar esse método com 1 parâmetro? (minha opinião)
console.log(palavra.substring(0)) // mostre-me tudo, como uma impressão normal
console.log(palavra.substring(1)) // mostre-me chars a partir do índice 1
console.log(palavra.substring(2)) // mostre-me chars a partir do índice 2

// Como eu devo pensar esse método com 2 parâmetros? (minha opinião)
console.log(palavra.substring(1, 3)) // mostre-me chars a partir do índice 1, até 3 - 1
